     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|unique:tags'
        ]);

        $tag = new Tag();
        $tag->name = $request->get("name");
        $tag->save();

        return redirect()->route("tags.index");
    }

